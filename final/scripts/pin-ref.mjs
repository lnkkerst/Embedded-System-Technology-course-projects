function extractNumber(s) {
  const splitter = c => /^[0-9]$/.test(c);
  const a = Array.from(s);
  return [
    a.filter(c => !splitter(c)).join(""),
    Number.parseInt(a.filter(splitter).join("")),
  ];
}

function main() {
  console.log(
    raw.sort((a, b) => {
      const [as, an] = extractNumber(a[0]);
      const [bs, bn] = extractNumber(b[0]);
      if (as > bs) {
        return 1;
      }
      if (as < bs) {
        return -1;
      }
      return an - bn;
    }).map(pin => `${pin[0]},${pin[1]}`).join("\n"),
  );
}

main();
